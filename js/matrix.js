const CARDINAL_AGE = {
  0: "day", 10: "nw", 20: "month", 30: "ne",
  40: "year", 50: "se", 60: "karma", 70: "sw",
};

// Свести число к диапазону 1..22 (22 НЕ сворачивается до 4 — это валидный аркан Шут).
function reduceArc(n) {
  while (n > 22) {
    n = String(n).split("").reduce((s, c) => s + Number(c), 0);
  }
  return n;
}

function digitSum(n) {
  return String(n).split("").reduce((s, c) => s + Number(c), 0);
}

function arc(n) {
  return { num: n, name: ARCANA[n][0] };
}

function meaning(n) {
  const [name, keywords, plus, minus] = ARCANA[n];
  return { num: n, name, keywords, plus, minus };
}

// Разбор даты: DD.MM.YYYY (точка/тире/слэш) или YYYY-MM-DD.
function parseDate(s) {
  s = s.trim();
  let m = s.match(/^(\d{1,2})[.\-/](\d{1,2})[.\-/](\d{4})$/);
  if (m) return { d: +m[1], mo: +m[2], y: +m[3] };
  m = s.match(/^(\d{4})[.\-/](\d{1,2})[.\-/](\d{1,2})$/);
  if (m) return { d: +m[3], mo: +m[2], y: +m[1] };
  throw new Error("Дата в формате ДД.ММ.ГГГГ");
}

function computeMatrix(d, mo, y) {
  const day = reduceArc(d);              // визитка (левая точка)
  const month = reduceArc(mo);           // таланты (верх)
  const year = reduceArc(digitSum(y));   // деньги / материальная карма (правая)
  const karma = reduceArc(day + month + year);          // главный урок (низ)
  const center = reduceArc(day + month + year + karma); // зона комфорта (центр)

  const nw = reduceArc(day + month);   // мама (верх-лево)
  const ne = reduceArc(month + year);  // папа (верх-право)
  const se = reduceArc(year + karma);  // низ-право
  const sw = reduceArc(karma + day);   // низ-лево

  const lichnoe = reduceArc(reduceArc(month + karma) + reduceArc(day + year));
  const social = reduceArc(reduceArc(nw + se) + reduceArc(ne + sw));
  const glavnoe = reduceArc(lichnoe + social);

  const t1 = reduceArc(center + karma); // вход в отношения
  const t2 = reduceArc(karma + t1);     // середина / самореализация
  const t3 = karma;                     // главное испытание

  const points = { day, month, year, karma, center, nw, ne, se, sw };

  const decades = Object.keys(CARDINAL_AGE)
    .map(Number).sort((a, b) => a - b)
    .map((age) => {
      const key = CARDINAL_AGE[age];
      return { age, point: key, ...arc(points[key]) };
    });

  const years = {};
  for (let Y = 2026; Y <= 2035; Y++) {
    // raw day используется (календарный день, не свёрнутый) — см. methodology.md
    years[Y] = arc(reduceArc(d + mo + digitSum(Y)));
  }

  // доминанта: арканы, встречающиеся >= 2 раз
  const counts = {};
  Object.values(points).forEach((n) => { counts[n] = (counts[n] || 0) + 1; });
  const dominant = Object.keys(counts)
    .map((n) => ({ num: +n, name: ARCANA[n][0], count: counts[n] }))
    .filter((x) => x.count >= 2)
    .sort((a, b) => b.count - a.count);

  const usedSet = new Set([...Object.values(points), lichnoe, social, glavnoe, t1, t2]);
  const arcana_meanings = {};
  [...usedSet].sort((a, b) => a - b).forEach((n) => { arcana_meanings[n] = meaning(n); });

  return {
    birthdate: `${String(d).padStart(2, "0")}.${String(mo).padStart(2, "0")}.${y}`,
    personal_square: {
      day_visitka: arc(day),
      month_talents: arc(month),
      year_money: arc(year),
      karma_lesson: arc(karma),
    },
    center_comfort: arc(center),
    ancestral_square: {
      nw_mother: arc(nw), ne_father: arc(ne), sw: arc(sw), se: arc(se),
    },
    purpose: {
      personal_to40: arc(lichnoe),
      social_from40: arc(social),
      main: arc(glavnoe),
    },
    karmic_tail: {
      entry_relationships: arc(t1),
      middle_selfrealization: arc(t2),
      main_test: arc(t3),
    },
    money_channel: {
      day: arc(day), center: arc(center), year: arc(year), realization: arc(karma),
    },
    age_decades: decades,
    personal_years: years,
    dominant,
    arcana_meanings,
  };
}

window.PhysalisMatrix = { ARCANA, reduceArc, parseDate, computeMatrix, meaning };
